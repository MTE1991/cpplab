#include <iostream>
using namespace std;

int main() {
    int a, b, c;
    cout << "Enter 3 int numbers: ";
    cin >> a >> b >> c;
    cout << "a = " << a << ", b = " << b << ", c = " << c << endl;
    double d, e, f;
    cout << "Enter 3 decimal numbers: ";
    cin >> d >> e >> f;
    cout << "d = " << d << ", e = " << e << ", f = " << f << endl;
    char g, h, i, j;
    cout << "Enter 4 chars: ";
    cin >> g >> h >> i >> j;
    cout << "g = " << g << ", h = " << h << ", i = " << i << ", j = " << j << endl;
    return 0;
}


